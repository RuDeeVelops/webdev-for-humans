// method chaining is a staple foundation of Javascript.
// it's written with .(dot) operator. and you can think of it as "do this thing on the previous thing"

document.getElementById("myElement").style.color = "red";

// Access "getElementById" on the document, then access "style" on the result, then do "color" on the result, then set it to "red".

// The above code is a simple example of method chaining.
