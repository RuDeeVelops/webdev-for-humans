// Logical operators in JavaScript

let a = true;
let b = false;

// && (AND)
// "Are both values true?"
// If both values are true, the result is true. Otherwise, the result is false.

console.log(a && a); // You can read this as: "Are both 'a' values true?"

// || (OR)
// "Is at least one value true?"
// If at least one value is true, the result is true. Otherwise, the result is false.

console.log(a || a); // You can read this as: "Is at least one 'a' value true?"

// ! (NOT)
// "Is the value false?"
// If the value is false, the result is true. Otherwise, the result is false.

console.log(!a); // You can read this as: "Is 'a' false?"

// === (EQUALS)
// "Are the values equal?"
// If the values are equal, the result is true. Otherwise, the result is false.

console.log(a === a); // You can read this as: "Are the 'a' values equal?"

// !== (NOT EQUALS)

console.log(a !== a); // You can read this as: "Are the 'a' values not equal?"

// > (GREATER THAN)

console.log(a > a); // You can read this as: "Is 'a' greater than 'a'?"

// < (LESS THAN)

console.log(a < a); // You can read this as: "Is 'a' less than 'a'?"

// >= (GREATER THAN OR EQUALS)

console.log(a >= a); // You can read this as: "Is 'a' greater than or equal to 'a'?"

// <= (LESS THAN OR EQUALS)

console.log(a <= a); // You can read this as: "Is 'a' less than or equal to 'a'?"
