// JSON
{
    "name": "John",
    "age": 30,
    "isStudent": false,
    "courses": ["Math", "English"]
}


// JavaScript Object
{
  name: 'John',
  age: 30,
  isStudent: false,
  courses: ['Math', 'English'],
  greet: function() {
    console.log('Hello, ' + this.name);
  }
}

// JSON vs JavaScript Objects

// JSON: Only supports strings, numbers, JSON objects, arrays, booleans, and null.
// JavaScript Objects: can also hold functions, Dates, and undefined.


// JSON: Property names must be double-quoted strings ("name"). Single quotes are not allowed.
// JavaScript Objects: Property names can be strings, numbers, or identifiers. Quotes are optional if the property name is a valid identifier. Both single and double quotes are allowed for strings.

